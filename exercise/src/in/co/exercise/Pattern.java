package in.co.exercise;

public class Pattern {
public static void main(String []args) {
	
	for(int a=0;a<=5;a++) 
	{
		for (int b=0;b<=a;b++) 
		{
			System.out.print("*");
			
		}
		System.out.println ();
	}
	

	for(int a=1;a<=5;a++) {
		
	for (int b=5;b>=a;b--) {
			
		System.out.print(b);
		}
		System.out.println();
	}
	
	
	
	}
}
	
